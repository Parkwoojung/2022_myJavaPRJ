public class Ex03_01 {
    public static void main(String[] args) {
        System.out.printf("100+100"); //모두 글자로 취급함
        System.out.printf("\n"); //다음 행에서 결과값 입력
        System.out.printf("%d", 100 + 100); //숫자로 계산해서 결과값 출력 %d
        System.out.printf("\n"); //다음 행에서 100+100 결과값 출력


    }
}
