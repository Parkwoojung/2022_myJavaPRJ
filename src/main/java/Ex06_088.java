public class Ex06_088 {
    public static void main(String[] args) {
        int hap = 0;
        int i;

        for (i = 1; i < 11; i++){
            hap +=1;
            System.out.println("hap 변수 값: " +hap);
            System.out.println("i 변수 값: " +i);
            System.out.println("-------------");
        }
        System.out.println("1부터 10까지의 합은" +hap);
    }
}
