public class For_1 {

    public static void main(String[] args) {

            //반복문


                for (int a = 3; a < 10; a++){
                    System.out.println(a+"단");

                    for (int b = 1; b < 10; b++) {

                        System.out.println(a+"*"+b+"="+ a*b);

                    }

                }
    }
}
