public class Wjstar_10 {
    public static void main(String[] args) {

        for (int k = 0; k < 10; k++){
            for (int y = 0; y < k; y++){
                System.out.print(" * ");
            }
            System.out.println(" ");
        }
        for (int i = 0; i < 10; i++) {
            for (int j = 0; j <10-i; j++) {
                System.out.print(" * ");

            }
            System.out.println(" ");




        }

    }
}
